#include <stdint.h>

#ifndef CA_MEMSTREAM
#define CA_MEMSTREAM

typedef struct {
    size_t      len;         // length of buffer in bytes
    size_t      pos;         // current byte position in buffer
    uint8_t     *data;       // pointer to bytes in memory
} memstream_buf_t;

#endif